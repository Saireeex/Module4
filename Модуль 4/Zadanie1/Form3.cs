using System;

// Интерфейс Книга
public interface IКнига
{
    // Метод для проверки доступности книги
    bool ДоступнаЛиКнига();

    // Метод для выдачи книги
    void ВыдатьКнигу();
}

// Класс для обычной книги
public class ОбычнаяКнига : IКнига
{
    private bool доступна;

    public ОбычнаяКнига(bool доступна)
    {
        this.доступна = доступна;
    }

    public bool ДоступнаЛиКнига()
    {
        return доступна;
    }

    public void ВыдатьКнигу()
    {
        if (доступна)
        {
            доступна = false;
            Console.WriteLine("Обычная книга выдана.");
        }
        else
        {
            Console.WriteLine("Обычная книга недоступна.");
        }
    }
}

// Класс для электронной книги
public class ЭлектроннаяКнига : IКнига
{
    private bool доступна;

    public ЭлектроннаяКнига(bool доступна)
    {
        this.доступна = доступна;
    }

    public bool ДоступнаЛиКнига()
    {
        return доступна;
    }

    public void ВыдатьКнигу()
    {
        if (доступна)
        {
            Console.WriteLine("Электронная книга выдана.");
        }
        else
        {
            Console.WriteLine("Электронная книга недоступна.");
        }
    }
}

// Основной класс программы
class Program
{
    static void Main(string[] args)
    {
        IКнига обычнаяКнига = new ОбычнаяКнига(true);
        IКнига электроннаяКнига = new ЭлектроннаяКнига(true);

        // Проверим доступность обычной книги и выдадим ее
        if (обычнаяКнига.ДоступнаЛиКнига())
        {
            обычнаяКнига.ВыдатьКнигу();
        }

        // Проверим доступность электронной книги и выдадим ее
        if (электроннаяКнига.ДоступнаЛиКнига())
        {
            электроннаяКнига.ВыдатьКнигу();
        }

        Console.ReadLine();
    }
}
